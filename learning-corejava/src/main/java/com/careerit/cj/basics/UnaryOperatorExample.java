package com.careerit.cj.basics;

public class UnaryOperatorExample {

    public static void main(String[] args) {

            int num = 10;
            System.out.println(num);
            num++;
            System.out.println(num);


            int i = 1;
            i++;
            i++;
            i++;
            i--;
            System.out.println(i);



    }
}
